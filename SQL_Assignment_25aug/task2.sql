--CREATE DATABASE PRODUCT_DB
USE PRODUCT_DB


CREATE TABLE ORDERS(
    ID INTEGER PRIMARY KEY,
    TOTAL INTEGER ,
    DATE DATE,
 );


CREATE TABLE CATEGORIES(

    ID INTEGER PRIMARY KEY,
    TITLE VARCHAR(30),
    DESCRIPTION VARCHAR(50)
);


CREATE TABLE PRODUCTS(
    ID INTEGER PRIMARY KEY,
    TITLE VARCHAR(20),
    PRICE DECIMAL(5,2),
    DESCRIPTION VARCHAR(50),
    CATEGORY INTEGER,
    COMPANY VARCHAR(50),
    FOREIGN KEY (CATEGORY) REFERENCES CATEGORIES(ID),

);


CREATE TABLE ORDER_DETAILS(
    ID INTEGER PRIMARY KEY,
    ORDERID INTEGER,
    PRODUCID INTEGER,
    QUANTITY INTEGER,
    PRICE DECIMAL(5,2),
    TOTALPRICE DECIMAL(5,2),
    DISCOUNT INTEGER,
    FOREIGN KEY (ORDERID) REFERENCES ORDERS(ID),
    FOREIGN KEY  (PRODUCID) REFERENCES PRODUCTS(ID)
);





