CREATE DATABASE STUDENT_DB
USE STUDENT_DB;
ALTER DATABASE DATA MODIFY NAME = STUDENT_DB;
CREATE TABLE SCHOOL(
    ID INTEGER PRIMARY KEY,
    NAME VARCHAR(20),
    ADDRESS VARCHAR(30),
    PRINCIPAL VARCHAR(20),
    PHONE INTEGER 
);


CREATE TABLE STUDENT(
    ID INTEGER PRIMARY KEY,
    NAME VARCHAR(20),
    ADDRESS VARCHAR(20),
    PHONE NUMERIC,
    EMAIL VARCHAR(30),
    AGE INTEGER,
    SCHOOLID INTEGER,
    FOREIGN KEY(SCHOOLID) REFERENCES SCHOOL(ID)

);
